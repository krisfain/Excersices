public class For {
    public static void main(String[] args){
        for (int i=0;i<=10;i=i+1){
            //условие (в скобках) содержит 3 части: переменная счетчик; условие до каких пор будет выполняться цикл; что мы будем делать с переменной i при каждом цикле
            //i=i+1 это тоже самое что i++
            System.out.println("Bingo"+i);
        }
    }
}
